#include <iostream>

using namespace std;

int main()
{
    int x,y;

    x=20;
    y=-10;

    cout<<"x>10 && y<0 :"<< (x>10 && y<0)<<endl;
    cout<<"x<10 || y<0 :"<< (x<10 || y<0)<<endl;
    cout<<"x>15 && y>0 :"<< (x>15 && y>0)<<endl;
    cout<<"x<15 || y>0 :"<< (x<15 || y>0)<<endl;




    return 0;
}
